a = 25*10^-6;
n2 = 1.44;
l = 850 * 10^-9;
NA = 0.33525;
L = 1000;
c = 3*10^8; 
n1 = sqrt(NA^2 + n2^2 );
delta = (n1 - n2) / n1;
v = 2*3.14*a*NA/l;
fprintf("n1 = %d",n1);
fprintf("\nnumber of fiber = %d",v);
modes = 1/2 * v^2;
fprintf("\nnumber of modes which can propagatein the fiber = %d",modes);
ci = n1*L*delta/c;
fprintf("\nintermodal dispersion = %d s/m",ci);
deltal = 40 * 10^-9;
dm = -85*10^-6;
deltacm = dm* deltal;
fprintf("\nmaterial dispersion = %d s/m",deltacm);
pb = sqrt(deltacm^2 + ci^2);
fprintf("\ntotal pulse broadening  = %d s/m",pb);